class Arbol:
    def __init__(self, dato:str):
        self.dato = dato
        self.derecha= None
        self.izquierda= None

def agregar_recursiva(self,Nodo,dato):
    if dato<  Nodo.dato:
        if Nodo.izquierda is None:
            Nodo.izquierda=Nodo(dato)
        else:
            self.agregar_recursiva(self,Nodo,dato)

    else:
        if Nodo.derecha is None:
            Nodo.derecha is None(dato)    
        else:
            self.agregar_recursiva(self,Nodo,dato)
            return Nodo